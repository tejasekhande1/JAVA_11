// Scanner Class

import java.util.*;
class Input{
	public static void main(String[] args){

		Scanner sc = new Scanner(System.in);

		int x = sc.nextInt();
		String y = sc.next();
		float z = sc.nextFloat();
		char a = sc.next().charAt(0);

		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(a);
	}
}


